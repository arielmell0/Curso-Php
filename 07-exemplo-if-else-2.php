<?php

	//== (igual)

	if(2==2){
		echo 'Sim, 2 é igual a 2 <br><br>';
	}else{
		echo 'Não, expressão é falsa <br><br>';
	}

	//=== (idêntico)

	if(2==='2'){
		echo 'Sim, 3 é igual a 2';
	}else{
		echo 'Não, expressão é falsa<br><br>';
	}

	if('x'!='y'){
		echo 'Sim, x é diferente de y';
	}

	/*não é obrigatório o uso do else, desse modo após ignorar o fluxo if, a expressão irá continuar, não retornando nada na tela*/

?>